package encapsulation;

public class Encapsulation {
	
	private int age=5;
	private String name="Izza";
	
	
	
	public int getAge() {
		return age;
	}
	public void setAge() {
		this.age = 20;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName() {
		this.name = "John";
	}
	
	

}
